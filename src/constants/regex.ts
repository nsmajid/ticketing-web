/**
 * --------------------------------------------------------------------------
 * Authentication
 * --------------------------------------------------------------------------
 */

export const AUTH_REGEX = {
  PASSWORD: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d).{8,}$/,
} as const

/**
 * --------------------------------------------------------------------------
 * User
 * --------------------------------------------------------------------------
 */

export const USER_REGEX = {
  EMAIL: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,

  USERNAME: /^[a-zA-Z0-9._-]{3,50}$/,

  NAME: /^[a-zA-ZÀ-ÿ\s'.-]{2,100}$/,

  PHONE: /^(\+62|62|0)8[1-9][0-9]{6,11}$/,
} as const

/**
 * --------------------------------------------------------------------------
 * Common
 * --------------------------------------------------------------------------
 */

export const COMMON_REGEX = {
  SLUG: /^[a-z0-9]+(?:-[a-z0-9]+)*$/,

  UUID: /^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i,

  NUMBER: /^\d+$/,

  DECIMAL: /^\d+(\.\d+)?$/,

  HEX_COLOR: /^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$/,

  URL: /^https?:\/\/.+$/i,
} as const

/**
 * --------------------------------------------------------------------------
 * File
 * --------------------------------------------------------------------------
 */

export const FILE_REGEX = {
  IMAGE: /\.(jpg|jpeg|png|gif|webp)$/i,

  PDF: /\.pdf$/i,

  DOCUMENT: /\.(pdf|doc|docx|xls|xlsx|ppt|pptx)$/i,
} as const
